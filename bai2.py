a = int(input("Nhập vào số nguyên a: "))
b = int(input("Nhập vào số nguyên b: "))
print("Tổng hai số a và b là: ", a+b)
print("Hiệu hai số a và b là: ", a-b)
print("Tích hai số a và b là: ", a*b)
print("Thương hai số a và b là: ", round(a/b,3))
print("Phần nguyên chia hai số a và b là: ", a//b)
print("Phần dư chia số a và b là: ", a%b)